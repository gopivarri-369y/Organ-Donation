'use strict';

let hover1 = document.querySelector('#navbar1');
// console.dir(hover);

// Use 'mouseenter' event to change the background color
hover1.addEventListener('mouseenter', function() {
    hover1.style.backgroundColor = "#03A9F4 ";  // Change background color to red on hover
});

// Optionally, reset the background color when the mouse leaves
hover1.addEventListener('mouseleave', function() {
    hover1.style.backgroundColor = "";  // Reset background color when mouse leaves
});

